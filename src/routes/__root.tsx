function RootShell({ children }: { children: React.ReactNode }) {
  return (
    <html lang="en">
      <head>
        <HeadContent />
      </head>
      <body>
        {children}
        <Scripts />
      </body>
    </html>
  );
}

function RootComponent() {
  const { queryClient } = Route.useRouteContext();

  return (
    <QueryClientProvider client={queryClient}>
      <SiteHeader />
      <DidYouKnowBubble />
      <main id="content">
        <Outlet />
      </main>
      <section id="email-signup" className="shell pb-20">
        <EmailSignup />
      </section>
      <SiteFooter />
    </QueryClientProvider>
  );
}
